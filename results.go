package mgo类

// X插入结果 is the result type returned by an InsertOne operation.
type X插入结果 struct {
	// The _id of the inserted document. A value generated by the driver will be of type primitive.ObjectID.
	X插入ID interface{}
}

// X插入多条结果 is a result type returned by an InsertMany operation.
type X插入多条结果 struct {
	// The _id values of the inserted documents. Values generated by the driver will be of type primitive.ObjectID.
	X插入IDs []interface{}
}

// X更新结果 is the result type returned from UpdateOne, UpdateMany, and ReplaceOne operations.
type X更新结果 struct {
	X匹配数  int64       // The number of documents matched by the filter.
	X修改数 int64       // The number of documents modified by the operation.
	X替换插入数 int64       // The number of documents upsert by the operation.
	X替换插入ID    interface{} // The _id field of the upsert document, or nil if no upsert was done.
}

// X删除结果 is the result type returned by DeleteOne and DeleteMany operations.
type X删除结果 struct {
	X删除数量 int64 // The number of documents deleted.
}
